#!/bin/bash
# 🚀 Aegis - Автоматический запуск сервера (Linux/macOS)
# Единый файл для запуска всего проекта

echo "============================================================"
echo "🛡️  AEGIS - Антивирусное браузерное расширение"
echo "============================================================"
echo "🚀 Запуск сервера..."
echo "============================================================"

# Проверяем наличие Python
if ! command -v python3 &> /dev/null; then
    echo "❌ Python3 не найден. Установите Python 3.8 или выше"
    exit 1
fi

# Проверяем наличие папки antivirus-core
if [ ! -d "antivirus-core" ]; then
    echo "❌ Папка antivirus-core не найдена"
    echo "💡 Запустите скрипт из корневой папки проекта"
    exit 1
fi

# Переходим в папку antivirus-core
cd antivirus-core

# Устанавливаем зависимости
echo "📦 Установка зависимостей..."
python3 -m pip install -r requirements.txt
if [ $? -ne 0 ]; then
    echo "❌ Ошибка установки зависимостей"
    exit 1
fi

echo "✅ Зависимости установлены"

# Запускаем сервер
echo "🌐 Запуск сервера..."
echo "📍 Адрес: http://127.0.0.1:8000"
echo "📚 Документация: http://127.0.0.1:8000/docs"
echo "🎛️  Admin UI: http://127.0.0.1:8000/admin/ui"
echo "💚 Статус: http://127.0.0.1:8000/health"
echo "============================================================"
echo "🔄 Для остановки нажмите Ctrl+C"
echo "============================================================"

python3 -m uvicorn app.main:app --host 127.0.0.1 --port 8000 --log-level info

echo ""
echo "🛑 Сервер остановлен"

